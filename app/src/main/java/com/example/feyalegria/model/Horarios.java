package com.example.feyalegria.model;

public class Horarios {
    public Integer idhorario;
    public String nombcurso;
    public String nomaula;
    public String hora_inicio;
    public String hora_fin;


    public Integer getIdhorario() {
        return idhorario;
    }

    public void setIdhorario(Integer idhorario) {
        this.idhorario = idhorario;
    }

    public String getNombcurso() {
        return nombcurso;
    }

    public void setNombcurso(String nombcurso) {
        this.nombcurso = nombcurso;
    }

    public String getNomaula() {
        return nomaula;
    }

    public void setNomaula(String nomaula) {
        this.nomaula = nomaula;
    }

    public String getHora_inicio() {
        return hora_inicio;
    }

    public void setHora_inicio(String hora_inicio) {
        this.hora_inicio = hora_inicio;
    }

    public String getHora_fin() {
        return hora_fin;
    }

    public void setHora_fin(String hora_fin) {
        this.hora_fin = hora_fin;
    }
}
